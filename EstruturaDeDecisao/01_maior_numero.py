# Faça um Programa que peça dois números e imprima o maior deles.

numero1 = int(input("Digite o 1º numero: "))
numero2 = int(input("Digite o 2º numero: "))

if numero1 > numero2:
    print(f"O {numero1} é maior entre os dois numeros digitados.")
else:
    print(f"O {numero2} é maior entre os dois numeros digitados.")
